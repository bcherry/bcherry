void _main(void) {
    int x,y;
    int tilewidth  = 16;
    int tileheight = 16;
    int vcount     = 6;
    int hcount     = 10;
    int i;
    if (!GrayMode(GRAY_ON)) return;
    memset(GetPlane(0),0,LCD_SIZE);
    memset(GetPlane(1),0,LCD_SIZE);
    for (y=0;y<vcount;y++) {
        for (x=0;x<hcount;x++) { 
            int offset = (y*hcount+x)*tileheight;
            for (i=0;i<2;i++) {
                Sprite16(x*tilewidth,y*tileheight,tileheight,&tiles[i][offset],GetPlane(i),SPRT_OR);
            }
        }
    }
    while (!kbhit());
    ngetchx();
    GrayMode(GRAY_OFF);
}

